CREATE TABLE EMPLEADOS(
	EMPID INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE1 VARCHAR(100),
    APELLIDO VARCHAR(100),
    GENERO VARCHAR(100),
    NACIMIENTO DATE NOT NULL
);

CREATE TABLE UBICACIONES(
	UBIID INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE2 VARCHAR(100),
    ESTADO VARCHAR(100)
);

