//No se debe especificar el tipo de variable
var variable=0;
//Estructuras de control de flujo
//Condicionales if
if(variable==1){console.log('Se cumple la condición');}
else{console.log('No se cumple la condición');}
//else-if
if(variable==1){console.log('Se cumple la condición');}
else if(variable==0 || variable==2){console.log('Condicional con o');}
else if(variable==0 && variable==3){console.log('Condicional con y');}
else{console.log('Otra condición activa');}
//Bucles for
for(var j=0;j<variable;j++){console.log('Ejecute esto mientras...');}
//while (condición de parada) Nota: ctrl+c rompe el bucle en caso de emergencia
while(variable!=0){console.log('variable es  diferente de cero');}

